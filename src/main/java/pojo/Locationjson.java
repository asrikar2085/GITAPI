package pojo;

public class Locationjson {
	
	private double lat;
	private double lng;
	
	public double getLat() {
		return lat;
	}
	public void setLat(double string) {
		this.lat = string;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double string) {
		this.lng = string;
	}
	

}
